class WPS3Uploader {
	static $instance = null;
	private $options = array(), $errors = array();

	function __construct() {
		global $wp_version;
		if (version_compare($wp_version, '2.5', '<=')) return;
	}

	public static function &factory() {
		if (self::$instance == null) {
			self::$instance = new WPS3Uploader;
			add_action('init', array(&self::$instance, '__init'));
		}
		return self::$instance;
	}

	public function __init() {
		add_action('admin_menu', array(&self::$instance, '__admin'));
	}

	public function __admin() {
		self::$instance->options = get_option('s3uploader');
		if (isset($this->options['accessKey'], $this->options['secretKey']) &&
		strlen($this->options['accessKey']) > 0 && strlen($this->options['secretKey']) > 0) {
			add_filter('media_upload_tabs', array(&self::$instance, '__addTab'));
			add_action('media_upload_s3', array(&self::$instance, '__getTab'));
		}
		// TODO: Change '8' access level to something more lenient?
		add_options_page('Amazon S3 Settings', 'Amazon S3', 8, 's3uploader', array(&self::$instance, '__options'));
		//add_submenu_page('upload.php', 'Amazon S3 Bucket', 'Amazon S3', 8, 's3uploader', array(&self::$instance, '__browse'));
	}

	public function __errorHandler($errNo, $errStr, $errFile, $errLine) {
		if (preg_match('/InvalidAccessKeyId/', $errStr)) $this->errors['invalidAccessInfo'] = 1;
		else $this->errors['unknown'] = 1;
	}

	public function __options() {
		if (isset($this->options['accessKey'], $this->options['secretKey']) &&
		strlen($this->options['accessKey']) > 0 && strlen($this->options['secretKey']) > 0) {
			S3::setAuth($this->options['accessKey'], $this->options['secretKey']);
			set_error_handler(array(&$this, '__errorHandler'));
			$buckets = S3::listBuckets();
			restore_error_handler();
		} else $buckets = array();

		$curlEnabled = true;

		if (isset($_POST['s3uploader_update'])) {
			$newoptions = $this->options;

			if (isset($_POST['s3uploader_accessKey'], $_POST['s3uploader_secretKey'])) {
				$newoptions['accessKey'] = trim(stripslashes($_POST['s3uploader_accessKey']));
				$newoptions['secretKey'] = trim(stripslashes($_POST['s3uploader_secretKey']));

				set_error_handler(array(&$this, '__errorHandler'));
				S3::setAuth($_POST['s3uploader_accessKey'], $_POST['s3uploader_secretKey']);
				$buckets = S3::listBuckets();
				restore_error_handler();
				
				if (isset($this->errors['invalidAccessInfo'])) {
					$this->options['accessKey'] = '';
					$this->options['secretKey'] = '';
					update_option('s3uploader', $this->options);
					$buckets = array();
				} else {
					$this->options = $newoptions;
					update_option('s3uploader', $newoptions);
				}
			}

			if (sizeof($this->errors) == 0) {
				if (is_array($buckets) && isset($_POST['s3uploader_bucket']) &&
				in_array($_POST['s3uploader_bucket'], $buckets)) {
					$newoptions['uploadBucket'] = trim($_POST['s3uploader_bucket']);
					$newoptions['bucketCNAME'] = (isset($_POST['s3uploader_bucketCNAME']) && (int)$_POST['s3uploader_bucketCNAME'] == 1) ? 1 : 0;
				} else {
					$this->errors['invalidBucket'] = 1;
				}
			}

			if (sizeof($this->errors) == 0 && $this->options != $newoptions) {
				$this->options = $newoptions; update_option('s3uploader', $this->options);
			}

			if (sizeof($this->errors) == 0) {
				echo "<div id=\"message\" class=\"updated fade\"><p>Settings saved.</p></div>";
			} else {
				if (isset($this->errors['invalidAccessInfo']))
					echo "<div id=\"message\" class=\"error\"><p>The AWS access information you have supplied is incorrect.</p></div>";
				elseif (isset($this->errors['invalidBucket']))
					echo "<div id=\"message\" class=\"error\"><p>Please select a valid Amazon S3 bucket which you will use to upload your data to.</p></div>";
			}

		} else {
			if (!extension_loaded('curl') && !@dl(PHP_SHLIB_SUFFIX == 'so' ? 'curl.so' : 'php_curl.dll')) {
				$curlEnabled = false;
				echo "<div id=\"message\" class=\"error\"><p>Please note: The <a href=\"http://php.net/curl\">CURL extension</a> is required to use and configure this plugin.  Please enable it or ask your system administrator to do so.</p></div>";
			} else $curlEnabled = true;
		}


		echo "<div class=\"wrap\">";
		echo "<h2>Amazon S3 Settings</h2>";

		echo '<form method="post" action="'.$_SERVER['REQUEST_URI'].'">';
		if (function_exists('wp_nonce_field')) wp_nonce_field('plugin-name-action_s3uploader');

		echo '<input type="hidden" name="s3uploader_update" value="1" />';

		echo '<br /><h3>AWS Access Information</h3>';
		echo '<table class="form-table">';
		echo '<tr valign="top">';
		echo '<th scope="row"><label for="s3uploader_accessKey">Access Key</a></th>';
		echo '<td><input name="s3uploader_accessKey" type="text" id="s3uploader_accessKey" class="code" value="'.(isset($this->options['accessKey']) ? $this->options['accessKey'] : '').'" size="70" '.(!$curlEnabled ? 'disabled="disabled" ' : '').'/></td>';
		echo '</tr>';

		echo '<tr valign="top">';
		echo '<th scope="row"><label for="s3uploader_secretKey">Secret Key</a></th>';
		echo '<td><input name="s3uploader_secretKey" type="text" id="s3uploader_secretKey" class="code" value="'.(isset($this->options['secretKey']) ? $this->options['secretKey'] : '').'" size="70" '.(!$curlEnabled ? 'disabled="disabled" ' : '').'/></td>';
		echo '</tr>';

		if (sizeof($buckets) > 0) {
			echo '<tr>';
			echo '<th scope="row"><label for="s3uploader_bucket">Upload Bucket</label></th>';
			echo '<td>';
			echo '<select name="s3uploader_bucket" id="s3uploader_bucket">';
			foreach ($buckets as $bucket) {
				if (isset($this->options['uploadBucket']) && $this->options['uploadBucket'] == $bucket)
					echo "<option value=\"{$bucket}\" selected=\"selected\">{$bucket}</option>";
				else
					echo "<option value=\"{$bucket}\">{$bucket}</option>";
			}
			echo '</select><br /><input name="s3uploader_bucketCNAME" id="s3uploader_bucketCNAME" type="checkbox" '.(isset($this->options['bucketCNAME']) && (int)$this->options['bucketCNAME'] == 1 ? 'checked="checked" ' : '').' '.(!$curlEnabled ? 'disabled="disabled" ' : '').'value="1" />&#160;<label for="s3uploader_bucketCNAME">Use CNAME alias for URLs</label>';
			echo '</td>';
			echo '</tr>';
		}

		echo '</table>';

		echo '<p class="submit"><input type="submit" name="Submit" value="Save Changes" '.(!$curlEnabled ? 'disabled="disabled" ' : '').'/>';
		echo '</form>';
		echo '</div>';
	}

	public function __addTab($tabs) {
		$tabs['s3'] = __('Amazon S3', 's3');
		return $tabs;
	}

	public function __getTab() {
		wp_enqueue_style('media');
		return wp_iframe(array(&$this, '__displayTab'), 'image', $errors);
	}


	private function __upload(&$upload, $maxAge = 315360000, $expires = '+3 years') {
		$saveName = date('Y/m/') . str_replace('%2F', '/', rawurlencode($upload['name']));
		$requestHeaders = array(
			'Content-Type' => $upload['type'],
			'Cache-Control' => 'max-age='.$maxAge,
			'Expires' => gmdate("D, d M Y H:i:s T", strtotime($expires))
		);
		S3::setAuth($this->options['accessKey'], $this->options['secretKey']);
		return S3::putObject(S3::inputFile($upload['tmp_name']), $this->options['uploadBucket'],
		$saveName, S3::ACL_PUBLIC_READ, array(), $requestHeaders) ?
		('http://'.($this->options['bucketCNAME'] ? $this->options['uploadBucket'] : $this->options['uploadBucket'].'.s3.amazonaws.com')) .
		'/'.$saveName : false;
	}

