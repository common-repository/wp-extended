<h2>XTENDED</h2>
<h3>About</h3>
<p>XTENDED is a simple plugin that allows you to extend Wordpress and integrate custom libraries like Zend Framework and Flourish libs... it was created because we needed to add out of the box functionality to WP for custom plugin, Templating and some freedom from the license restrictions of the GPL, a key requirements for using Wordpress in commercial development. XTENDED is created by Shawn Sandy and is supported by  <strong><a href="http://www.xsstreamstudio.com">XSSTREAMSTUDIO</a></strong> an Interactive Design Studio!</p>
<h3>Features</h3>
<ul>
  <ul style="font-weight:bold; color:#444; font-style:italic; font-family:Georgia, 'Times New Roman', Times, serif">
    <li>Auto loads Libraries (flourish default) Zend optional</li>
    <li>Plugin Functionality Activate/Deactivate nothing breaks on upgrade.</li>
    <li>Add or create your own custom libraries</li>
    <li>Add theming support to wordpress</li>
    <li>Easy to extend</li>
    <li>Structure to go, take your custom libraries anywhere</li>
    <li>Freedom from GPL license restrictions</li>
    <li>I will think of some more...</li>
  </ul>
  </ul>
